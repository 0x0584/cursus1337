#include "libft.h"

bool	is_prefix_sign(char c)
{
	return (c == '+' || c == '-' || c == ' ');
}

size_t	str_len_diff(const char *s1, const char *s2)
{
	return (s2 ? (size_t)(s2 - s1 - 1) : ft_strlen(s1));
}
